        <h5 style="text-align: justify; color: #008080;"><strong>Introduction</strong></h5>
        <div id="publication-freequency"></div>
        <p style="text-align: justify; color: #111;">IUBAT Review-A Multidisciplinary Academic journal is a peer reviewed journal affiliated to IUBAT-international University of Business Agriculture and Technology, Dhaka, Bangladesh. As a multidisciplinary journal, it intends to publish articles, review books and research papers in the fields of Physics, Chemistry, Mathematics, Statistics, Computer Science and Engineering, Civil Engineering, Mechanical Engineering, Electrical Engineering, English Language, English Literature, Linguistics, Economics, Agricultural Sciences, Tourism and Hospitality Management.</p>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Publication Freequency</strong></h5>
        <div id="aims"></div>
        <p style="text-align: justify; color: #111;">It is an annual print and online journal which is published in October every year. The journal is accessible also from the website</p>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Aims</strong></h5>
        <div id="benefitstoauthor"></div>
        <p style="text-align: justify; color: #111;">It aims to address the most important issues in the aforementioned fields. The journal can be of great value to teachers, students, researchers, and experts dealing with these fields</p>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Benefits to Author</strong></h5>
        <div id="conditions"></div>
        <p style="text-align: justify; color: #111;">The journal provides free publication benefit with a liberal copyright policy to the authors.</p>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Conditions for submission of manuscript</strong></h5>
        <div id="ethicalaspects"></div>
        <p style="text-align: justify; color: #111;">Manuscript are received with the explicit understanding that they are not under simultaneous consideration by any other publisher.</p>
        <p style="text-align: justify; color: #111;">Submission of a manuscript for publication implies the transfer of the copyright from the author to the publisher upon acceptance. Accepted manuscripts become the permanent property of the IUBAT journal and manuscriptsas a whole or in part may not be reproduced by author in any means without the written consent of the publisher.</p>
        <p style="text-align: justify; color: #111;">It is the authorâ€™s responsibility to obtain permission to reproduce illustration, tables etc. from other publications.</p>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Ethical aspects</strong></h5>
        <div id="manuscriptsubmission"></div>
        <p style="text-align: justify; color: #111;">Ethical aspects of the study will be carefully considered at the time of assessment of the manuscript.</p>
        <p style="text-align: justify; color: #111;">Any manuscript that includes table, illustration or photograph that has been published earlier should include a letter of permission for re-publication from the authors of the publication and editor/publisher of the journal where it was published earlier.</p>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Manuscript submission</strong></h5>
        <div id="coveringletter"></div>
        <p style="text-align: justify; color: #111;">The authors are requested to follow the submission Guidelines strictly. The following documents with manuscripts are to be submitted for publication:</p>
        <ul style="color: #111;">
        <li>A covering letter addressed to the Editor of the journal</li>
        <li>A Title page</li>
        <li>Abstract and key words in the first page.</li>
        <li>Authors must submit copies of all documents in electronic form as pdf or Word file.</li>
        </ul>

        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Covering letter</strong></h5>
        <div id="manuscript-organization"></div>
        <ul style="color: #111;">
        <li>All authors must signify that they are the only authors of the manuscript.</li>
        <li>The corresponding author should mention the contribution of each author to the work.</li>
        <li>The covering letter should contain a declaration that this manuscript has not been submitted elsewhere or not under consideration in any journal.</li>
        <li>It should clearly indicate the publication type (Original/Review/Case report/Letter etc.)</li>
        <li> It should also mention the expected benefits to the corresponding field through publishing this article.</li>
        </ul>

        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Manuscript Organization</strong></h5>
        <div id="manuscript-format"></div>
        <p style="text-align: justify; color: #111;">Typing</p>
            <ul style="color: #111;">
            <li>Double spaced throughout with justified and 2.5 cm margins.</li>
            <li>Font type is Times New Roman with size 12.</li>
            <li>Manuscript should be carefully proof read for grammar, spelling and punctuation.</li>
            </ul>

        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Manuscript Format</strong></h5>
        <div id="title-page"></div>
        <p style="text-align: justify; color: #111;">In general, original article should be divided into the following sections: Title page, Abstract, Text, Acknowledgement, Reference, Tables with titles and foot notes, alternatively graph with title and illustrations with legends. Each of the sections is to start on a separate page. Pages should be numbered consecutively beginning from the abstract.</p>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Title Page</strong></h5>
        <div id="abstract"></div>
        <ul style="color: #111;">
        <li>Title of the article</li>
        <li>Names of all authors with their designation and institutional affiliations</li>
        <li>Name of the department and institute where the authors are affiliated.</li>
        <li>Name of corresponding author with contact address, Telephone number, e-mail address</li>
        <li>Disclosure of conflict of interest if any</li>
        <li>Disclosure of sources of funding or sponsor</li>
        <li>Number of tables given</li>
        <li>Number of figure given</li>
        <li>word counts of abstract</li>
        </ul>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Abstract</strong></h5>
        <div id="text-language"></div>
        <p style="text-align: justify; color: #111;">Structured with headings (Background, Objectives, Methods with statistical analysis, Result)</p>
        <ul style="color: #111;">
        <li>Authors name should not be given</li>
        <li>Preferably within 150 words</li>
        <li>Avoid abbreviations in the title and abstract except standard abbreviation.</li>
        </ul>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Text Language (usage and editing services)</strong></h5>
        <div id="introduction1"></div>
        <p style="text-align: justify; color: #111;">Please write your text in good English (American or British usage is accepted, but not mixture of these two varieties). Authors who feel their manuscript requires editing to eliminate possible grammatical or spelling errors and to conform correct English, may wish to contact the Editor of this journal.</p>
        <p style="text-align: justify; color: #111;">Text should be arranged into following sections:</p>
        <p style="text-align: justify; color: #111;">Introduction, Methods, Results, Discussions, and Acknowledgement &amp; References.</p>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Introduction</strong></h5>
        <div id="method"></div>
        <ul style="color: #111;">
        <li>Statement of the problem with a short discussion of its importance&#8217;s and significance</li>
        <li>Review of the literature related to the problem with pertinent references</li>
        </ul>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Method</strong></h5>
        <div id="results"></div>
        <p style="text-align: justify; color: #111;">Method Should contain:</p>
        <ul  style="color: #111;">
        <li>Study type, place and time</li>
        <li>Description of study variables</li>
        <li>Description of study subjects and grouping</li>
        <li>Selection criteria</li>
        <li>Descriptions of procedures, methods, apparatus, drugs or chemicals applicable where necessary.</li>
        <li>Description of statistical procedures with enough details to enable a knowledgeable reader with access to the original data to verify the reported results.</li>
        <li>Experimental method should be detailed for the scientific articles.</li>
        </ul>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Results</strong></h5>
        <div id="table"></div>
        <ul>
        <li>Present result in a logical sequence in text, table and illustration with most important finding first.</li>
        <li>Describe without comment</li>
        <li>Restrict the number of tables and figures that are needed to support the manuscript</li>
        <li>Do not duplicate data in table and figure</li>
        </ul>
        </div>


        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Table</strong></h5>
        <div id="illustration"></div>
        <ul style="color: #111;">
        <li>Simple self explanatory with brief title, not duplicate in text</li>
        <li>Each table should be numbered in Romans.</li>
        <li>Uses of many tables are not encouraged.</li>
        </ul>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Illustration/Discussion</strong></h5>
        <div id="acknowledgement"></div>
        <ul style="color: #111;">
        <li>All illustration must be numbered consecutively in numerals as they appear in the next</li>
        </ul>
        <p style="text-align: justify; color: #111;">Divide your article into clearly defined and numbered sections. Subsections should be numbered 1.1 (then 1.1.1, 1.1.2, &#8230;), 1.2, etc. (the abstract should not be included in section numbering). Use this numbering for internal cross-referencing: do not just refer to &#8216;the text&#8217;. Any subsection may be given a brief heading. Each heading should appear on its own separate line.)</p>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Acknowledgement</strong></h5>
        <div id="reference-style"></div>
        <p style="text-align: justify; color: #111;">Individuals, institution, sponsor, organization or bodies can be acknowledged in the article for their contribution of financial or any form of assistance to the work.</p>
        </div>

        <div>
        <h5 style="text-align: justify; color: #008080;"><strong>Reference style (APA Style)</strong></h5>
        <p style="text-align: justify; color: #111;"><strong>Reference Citations in the Text</strong></p>
        <ul style="color: #111;">
        <li>Single author: Use the author&#8217;s last name, year: (Morse, 1996) OR Morse (1996)</li>
        <li>Two authors: Use both authors&#8217; last names, separated by an ampersand if in parentheses.</li>
        </ul>
        <p style="text-align: justify; color: #111;">(Ringsven &amp; Bond, 1996) OR Ringsven and Bond (1996)</p>
        <ul style="color: #111;">
        <li>Three to five authors: Use all authors&#8217; names and year, the first time the reference occurs; in subsequent citations, include only the first author followed by &#8220;et al.&#8221; and the year.</li>
        </ul>
        <p style="text-align: justify; color: #111;">Example: First citation: (Johnson, Brunn, &amp; Platt, 2002) OR Johnson, Brunn and Platt (2002)</p>
        <p style="text-align: justify; color: #111;">Subsequent citations: (Johnson et al., 2002).</p>
        <ul style="color: #111;">
        <li>Six or more authors: Use only the first author followed by et al. and the year.</li>
        </ul>
        <p style="text-align: justify; color: #111;">(Arpin et al., 2001) OR Arpin et al. (2001) If two references with the same year shorten to the same form, cite the name of the first authors and as many of the subsequent authors as necessary to distinguish the two references, followed by a comma and et al.</p>
        <ul style="color: #111;">
        <li>Groups as authors: Corporation, association, and government names are given in full in the first citation, and may be abbreviated thereafter if the name is long.</li>
        <li>(The Michener Institute, 2002) OR The Michener Institute (2002) reported that&#8230;</li>
        <li>Personal communication uses of personal communication as a citation should be avoided, unless it provides essential information not available from a public source. Do not include it in the reference list; instead cite the last name and initials of the person and date of communication in parentheses in the text.</li>
        <li>(T. K. Lutes, personal communication, September 28, 1998) OR</li>
        <div id="reference-example"></div>
        <li>T.K. Lutes (personal communication, September 28, 1998)</li>
        <li>Direct quotes are to be used very sparingly. Incorporate short quotes of fewer than 40 words into the text and place quotation marks around the quote. Quote 40 or more words in a double-spaced block of text indented 5 spaces from the left margin, without quotation marks. Give specific page numbers.</li>
        <li>&#8220;quote&#8221; (Miele, 1993, p. 276) OR Miele (1993) found that &#8220;quote&#8221; (p. 276).</li>
        </ul>
        </div>

