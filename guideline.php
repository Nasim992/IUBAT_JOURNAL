        <h6 style="font-weight:bold;"> Reference style (APA Style)</h6>
        <p>
        <b>Reference Citations in the Text</b> <br>
        </p>
        <li>Single author: Use the author’s last name, year: (Morse, 1996) OR Morse (1996)</li><br>
        <p>(Ringsven & Bond, 1996) OR Ringsven and Bond (1996)</p><br>
        <li>Two authors: Use both authors’ last names, separated by an ampersand if in parentheses.</li>
        <li>Three to five authors: Use all authors’ names and year, the first time the reference occurs; in subsequent citations, include only the first author followed by “et al.” and the year.</li> <br>
        <p>Example: First citation: (Johnson, Brunn, & Platt, 2002) OR Johnson, Brunn and Platt (2002)</p><br>
        <p>Subsequent citations: (Johnson et al., 2002).</p><br>
        <li>Six or more authors: Use only the first author followed by et al. and the year.</li><br>
        <p>(Arpin et al., 2001) OR Arpin et al. (2001) If two references with the same year shorten to the same form, cite the name of the first authors and as many of the subsequent authors as necessary to distinguish the two references, followed by a comma and et al.</p><br>
        <li>Groups as authors: Corporation, association, and government names are given in full in the first citation, and may be abbreviated thereafter if the name is long.</li>
        <li>(The Michener Institute, 2002) OR The Michener Institute (2002) reported that…</li>
        <li>Personal communication uses of personal communication as a citation should be avoided, unless it provides essential information not available from a public source. Do not include it in the reference list; instead cite the last name and initials of the person and date of communication in parentheses in the text.</li>
        <li>(T. K. Lutes, personal communication, September 28, 1998) OR</li>
        <li>T.K. Lutes (personal communication, September 28, 1998)</li>
        <li>Direct quotes are to be used very sparingly. Incorporate short quotes of fewer than 40 words into the text and place quotation marks around the quote. Quote 40 or more words in a double-spaced block of text indented 5 spaces from the left margin, without quotation marks. Give specific page numbers.</li>
        <li>“quote” (Miele, 1993, p. 276) OR Miele (1993) found that “quote” (p. 276).</li>
