        <div id="essentialtitlepageinformation">
        <h6 style="font-weight:bold;">Essential title page information</h6>
        <li><b><i>Title. </i></b>Concise and informative. Titles are often used in information-retrieval systems. Avoid abbreviations and formulae where possible.</li>
        <li><b><i>Author names and affiliations. </i></b> Please clearly indicate the given name(s) and family name(s) of each author and check that all names are accurately spelled. You can add your name between parentheses in your own script behind the English transliteration. Present the authors' affiliation addresses (where the actual work was done) below the names. Indicate all affiliations with a lower-case superscript letter immediately after the author's name and in front of the appropriate address. Provide the full postal address of each affiliation, including the country name and, if available, the e-mail address of each author.</li>
        <li><b><i>Corresponding author. </i></b> Clearly indicate who will handle correspondence at all stages of refereeing and publication, also post-publication. This responsibility includes answering any future queries about Methodology and Materials. Ensure that the e-mail address is given and that contact details are kept up to date by the corresponding author.</li>
        <li><b><i>Present/permanent address. </i></b>  If an author has moved since the work described in the article was done, or was visiting at the time, a 'Present address' (or 'Permanent address') may be indicated as a footnote to that author's name. The address at which the author actually did the work must be retained as the main, affiliation address. Superscript Arabic numerals are used for such footnotes.</li>
        </div>
        <div id="abstract">
        <h6 style="font-weight:bold;">Abstract</h6>
        <p>A concise and factual abstract is required. The abstract should state briefly the purpose of the research, the principal results and major conclusions. An abstract is often presented separately from the article, so it must be able to stand alone. For this reason, References should be avoided, but if essential, then cite the author(s) and year(s). Also, non-standard or uncommon abbreviations should be avoided, but if essential they must be defined at their first mention in the abstract itself.</p>
        </div>
        <div id="keywords">
        <h6 style="font-weight:bold;">Keywords</h6>
        <p>Immediately after the abstract, provide a maximum of 6 keywords, using American spelling and avoiding general and plural terms and multiple concepts (avoid, for example, 'and', 'of'). Be sparing with abbreviations: only abbreviations firmly established in the field may be eligible. These keywords will be used for indexing purposes.</p>
        </div>
        <div id="abbreviations">
        <h6 style="font-weight:bold;">Abbreviations</h6>
        <p>Define abbreviations that are not standard in this field in a footnote to be placed on the first page of the article. Such abbreviations that are unavoidable in the abstract must be defined at their first mention there, as well as in the footnote. Ensure consistency of abbreviations throughout the article.</p>
        </div>
        <div id="acknowledgement">
        <h6 style="font-weight:bold;">Acknowledgement</h6>
        <p>Collate acknowledgements in a separate section at the end of the article before the references and do not, therefore, include them on the title page, as a footnote to the title or otherwise. List here those individuals who provided help during the research (e.g., providing language help, writing assistance or proof reading the article, etc.)</p>
        </div>
        <div id="mathformulae">
        <h6 style="font-weight:bold;">Math Formulae</h6>
        <p>Please submit math equations as editable text and not as images. Present simple formulae in line with normal text where possible and use the solidus (/) instead of a horizontal line for small fractional terms, e.g., X/Y. In principle, variables are to be presented in italics. Powers of e are often more conveniently denoted by exp. Number consecutively any equations that have to be displayed separately from the text (if referred to explicitly in the text).</p>
        </div>
        <div id="footnotes">
        <h6 style="font-weight:bold;">Footnotes</h6>
        <p>Footnotes should be used sparingly. Number them consecutively throughout the article. Many word processors can build footnotes into the text, and this feature may be used. Otherwise, please indicate the position of footnotes in the text and list the footnotes themselves separately at the end of the article. Do not include footnotes in the Reference list.</p>
        </div>
        <div id="artwork">
        <h6 style="font-weight:bold;">Artwork</h6>
        <h6 style="font-weight:bold;"><i>Electronic Artwork</i></h6>
        <li> Make sure you use uniform lettering and sizing of your original artwork.</li>
        <li> Embed the used fonts if the application provides that option.</li>
        <li> Aim to use the following fonts in your illustrations: Arial, Courier, Times New Roman, Symbol, or use fonts that look similar.</li>
        <li> Number the illustrations according to their sequence in the text.</li>
        <li> Use a logical naming convention for your artwork files.</li>
        <li>Provide captions to illustrations separately.</li>
        <li> Size the illustrations close to the desired dimensions of the published version.</li>
        <li>Submit each illustration as a separate file.</li>
        <li> Ensure that color images are accessible to all, including those with impaired color vision.</li>
        <P>
        A detailed guide on electronic artwork is available.
You are urged to visit this site; some excerpts from the detailed information are given here.
Formats
If your electronic artwork is created in a Microsoft Office application (Word, PowerPoint, Excel) then please supply 'as is' in the native document format.
Regardless of the application used other than Microsoft Office, when your electronic artwork is finalized, please 'Save as' or convert the images to one of the following formats (note the resolution requirements for line drawings, halftones, and line/halftone combinations given below):
EPS (or PDF): Vector drawings, embed all used fonts.
TIFF (or JPEG): Color or grayscale photographs (halftones), keep to a minimum of 300 dpi.
TIFF (or JPEG): Bitmapped (pure black & white pixels) line drawings, keep to a minimum of 1000 dpi.
TIFF (or JPEG): Combinations bitmapped line/half-tone (color or grayscale), keep to a minimum of 500 dpi.
        </P>
        <h6 style="font-weight:bold;">Please do not : </h6>
        <li>Supply files that are optimized for screen use (e.g., GIF, BMP, PICT, WPG); these typically have a low number of pixels and limited set of colors;</li>
        <li>Supply files that are too low in resolution;
        </li>
        <li>Submit graphics that are disproportionately large for the content.
        </li>
        <h6 style="font-weight:bold;"><i>Color Artwork</i></h6>
        <p>Please make sure that artwork files are in an acceptable format (TIFF (or JPEG), EPS (or PDF) or MS Office files) and with the correct resolution. If, together with your accepted article, you submit usable color figures then Elsevier will ensure, at no additional charge, that these figures will appear in color online (e.g., ScienceDirect and other sites) in addition to color reproduction in print. Further information on the preparation of electronic artwork.</p>
        <h6 id="tables" style="font-weight:bold;"><i>Tables</i></h6>
        <p>Please submit tables as editable text and not as images. Tables can be placed either next to the relevant text in the article, or on separate page(s) at the end. Number tables consecutively in accordance with their appearance in the text and place any table notes below the table body. Be sparing in the use of tables and ensure that the data presented in them do not duplicate results described elsewhere in the article. Please avoid using vertical rules and shading in table cells.</p>
        <h6 id="figurecaptions" style="font-weight:bold;"><i>Figure captions</i></h6>
        <p>Ensure that each illustration has a caption. Supply captions separately, not attached to the figure. A caption should comprise a brief title (not on the figure itself) and a description of the illustration. Keep text in the illustrations themselves to a minimum but explain all symbols and abbreviations used.</p>
