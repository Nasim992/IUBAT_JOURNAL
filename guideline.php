        <h6  style="font-weight:bold;"><i>Reference Style</i></h6>
        <p>Text: Indicate references by number(s) in square brackets in line with the text. The actual authors can be referred to, but the reference number(s) must always be given.
        Example: "..... as demonstrated [3,6]. Barnaby and Jones [8] obtained a different result ...."
        List: Number the references (numbers in square brackets) in the list in the order in which they appear in the text.
        Examples:
       1- J. van der Geer, J.A.J. Hanraads, R.A. Lupton, The art of writing a scientific article, J. Sci. Commun. 163 (2000) 51–59.
       2- W. Strunk Jr., E.B. White, The Elements of Style, third ed., Macmillan, New York, 1979.</p>
